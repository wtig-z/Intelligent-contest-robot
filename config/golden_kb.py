# ---------- 统计：同义词 → 标准「赛事类别」（供 curated_database_query 调用）----------
_STAT_LABEL: dict[str, str] = {
    "青少年人工智能": "人工智能",
    "数学建模": "数学建模",
    "数据挖掘": "数据挖掘",
    "电子信息": "电子信息",
    "创新创业": "创新创业",
}


def match_standard_category_from_text(text: str) -> Optional[str]:
    """从用户问句中按最长同义词优先匹配标准赛事类别。"""
    q = (text or "").replace(" ", "").strip()
    if not q:
        return None
    for key in sorted(CATEGORY_STANDARD_MAP.keys(), key=len, reverse=True):
        if key in q:
            return CATEGORY_STANDARD_MAP[key]
    return None


def stat_filter_rows_from_question(question: str, db: list[dict]) -> Optional[list[dict]]:
    """
    从完整问句匹配标准类，返回 db 中 赛事类别 相等的行；无映射则返回 None（交由旧逻辑）。
    """
    std = match_standard_category_from_text(question)
    if not std:
        return None
    out = [r for r in db if str(r.get("赛事类别") or "") == std]
    if not out:
        return None
    return out


def count_answer_domain_label(std_category: str) -> str:
    """统计类回答中的领域称谓。"""
    return _STAT_LABEL.get(std_category, std_category or "")
